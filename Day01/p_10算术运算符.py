'''
算术运算符
'''

# + 加法
print(1 + 2) # 3
print('1' + '2') #  '12'
# print('1' + 1) # 错  TypeError: must be str, not int

# - 减法
print(1 - 2)  # -1

# 乘法
print(3 * 5) # 15
print('a' * 3) # 'aaa'
# print('a' * 'b') # 错 TypeError: can't multiply sequence by non-int of type 'str'


# 除法
print(4 / 2) # 2.0
print(10 / 3) # 3.33

# 取整除（地板除）

print(4 // 2) # 2
print(10 // 3) # 3

# % 取余（取模，求模）
print(9 % 2)


# 乘方  **
print(2 ** 2)
print(2 ** 3)
print(2 ** 4)
print(2 ** 5)
print(2 ** 6)
print(2 ** 7)